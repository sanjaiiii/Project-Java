package Loops;

public class S3 {

	public static void main(String[] args) {
		
		char initial='A';
		switch(initial)
		{
		case 'k':
			   System.out.println("The initial for divya is K");
		       break;
		case 'A':
			   System.out.println("The initial for divya is A");
		       break;
		case 'C':
			   System.out.println("The initial for divya is C");
		       break;
		default:
			System.out.println("U have entered a wrong initial");
			break;
			
		}

	}

}
