package Loops;

public class W1 {

	public static void main(String[] args) {
		//Initialisation : Where to start
		//Condition      : Where to stop
		//1)forward  - <=
		//2)backward - >=
		//inc/dec : Difference between the numbers
		//WHILE LOOP
		//Syntax:
		      //-->initialisation
		      //while(condtion)
		      //{inc/dec}
		 
			
		
		
		int i=50;
		while (i<=60) 
		{
			System.out.println(i);
			i=i+2;
		}
		
		int a=1;
		while(a<=13) 
		{
			
			System.out.println(a);
			a=a+2;
		}
		
		int b=7;
		while(b<=70) 
		{
			System.out.println(b);
			b=b+7;
		}

		int c=13;
		while(c>=1)
		{
			System.out.println(c);
			c=c-2;
			
		}
		
	}

	
}
