package Loops;

public class f1 {

	public static void main(String[] args) {
		//Initialisation : Where to start
				//Condition      : Where to stop
				//1)forward  - <=
				//2)backward - >=
				//inc/dec : Difference between the numbers
		
		//Syntax: for(initalisation;condition;inc/dec){}
		
		for(int i=20; i>=1; i=i-1)
		{
			System.out.println(i);
			     }
		

		for(int a=2; a<=100; a=a+2)
		{System.out.println(a);}
		
		
	for(int z=11;z<=110;z=z+11) 
	  {System.out.println(z);}
	
	

	
	}

}
